// Helper functions
export function getAgenciesByCity(citySlug: string): OklahomaAgency[] {
  const cityData = OKLAHOMA_CITIES.find(c => c.slug === citySlug);
  return cityData?.agencies || [];
}

export function getAgencyBySlug(slug: string): OklahomaAgency | undefined {
  return ALL_OKLAHOMA_AGENCIES.find(a => a.slug === slug);
}

export function searchAgencies(query: string): OklahomaAgency[] {
  const lowerQuery = query.toLowerCase();
  return ALL_OKLAHOMA_AGENCIES.filter(agency =>
    agency.name.toLowerCase().includes(lowerQuery) ||
    agency.city.toLowerCase().includes(lowerQuery) ||
    agency.address.toLowerCase().includes(lowerQuery)
  );
}

