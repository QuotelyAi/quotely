// Helper to generate slug (exported for external use)
export const toSlug = (str: string): string =>
  str.toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();

